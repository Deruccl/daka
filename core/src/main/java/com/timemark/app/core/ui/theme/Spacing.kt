package com.timemark.app.core.ui.theme

import androidx.compose.ui.unit.dp

/**
 * 间距系统
 *
 * 统一应用内各组件的间距规范，保证视觉节奏一致：
 * - xs (4dp)：紧凑间距（图标与文字）
 * - sm (8dp)：小间距（相关元素间）
 * - md (16dp)：标准间距（卡片内边距）
 * - lg (24dp)：大间距（区块间）
 * - xl (32dp)：超大间距（页面区块间）
 * - xxl (48dp)：页面级间距
 * - xxxl (64dp)：最大间距
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 16.dp
    val lg = 24.dp
    val xl = 32.dp
    val xxl = 48.dp
    val xxxl = 64.dp
}
